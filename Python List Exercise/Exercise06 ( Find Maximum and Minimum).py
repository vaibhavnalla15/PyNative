"""  Find Maximum and Minimum """
# Find and print the largest and smallest number in a list [8, 2, 15, 1, 9].
# Hints:- Python provides built-in functions max() and min() for finding the maximum and minimum values in a list.

data = [8, 2, 15, 1, 9]

print("Largest number:-", max(data))
print("Smallest number:-", min(data))
